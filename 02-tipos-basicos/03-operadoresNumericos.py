#ejemplos de operadores numericos en py
#declaramos algundas variables para operar
#
#   RESUMEN  
# =============
#
#   +  suma               -   resta (negacion)
#   *  multiplicacion     **  potencia
#   /  division           //  division Entera
#   %  modulo
#

entero1=11
entero2=22
float1=1.01
float2=2.02

#--------------------------------------
#
# SUMA. usando el simbolo *
#
print("\n ===== Ejemplo de Suma ======= ")
print(entero1+entero2)
print(float1+float2)
print(entero1+float1)

#--------------------------------------
#
# RESTA, usando un - entre dos valores
#
print("\n ===== Ejemplo de Resta ====== ")
print(entero1-entero2)
print(float1-float2)
print(entero1-float1)

#--------------------------------------
# 
# NEGACION ,. un -antes que un valor
#
print("\n ==== Ejemplo de negacion ====")
print(-entero1)
print(-float1)

#--------------------------------------
#
# MULTIPLICACION usando * entre dos valores
#
print("\n == Ejemplo de Multiplicacion == ")
print(entero1*entero2)
print(float1*float2)
print(entero1*float1)

#--------------------------------------
#
# POTENCIA usando ** entre dos valores
#
print("\n ==== Ejemplo de potencia ==== ")
print(entero1**entero2)
print(float1**float2)
print(entero1**float1)
print(float2**entero2)

#--------------------------------------
#
# DIVISION usando / entre dos valores
#
print("\n ==== Ejemplo de division ==== ")
print(entero1/entero2)
print(float1/float2)
print(entero1/float1)
print(float2/entero2)

#--------------------------------------
#
# division entera usando // entre dos valores
#
print("\n === ejemplo de divi entera === ")
print(entero1//entero2)
print(float1//float2)
print(entero1//float1)
print(float2//entero2)

#--------------------------------------
#
# division entera usando // entre dos valores
#
print("\n ===== ejemplo de modulo ===== ")
print(entero1%entero2)
print(float1%float2)
print(entero1%float1)
print(float2%entero2)



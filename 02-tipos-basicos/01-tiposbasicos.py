#algunos tipos basicos que pueden ser de utilidad 

# NOTA.- a diferencia de otros lengs en python no es necesario
#        declarar el tipo de objeto
#
#   * el operador type(objeto) devuelve el tipo de objeto 
#

#numeros enteros -------------------------------------------
numeroEntero=1
print("\nNumero entero: ------------------------------")
print(numeroEntero) 
print(type(numeroEntero))

#numeros complejos------------------------------------------
numeroComplejo=3+3j
print("\nNumero complejo: ----------------------------")
print(numeroComplejo)
print(type(numeroComplejo))

#numeros de coma flotante-----------------------------------
numeroComaFlotante=3.12345
print("\nNumero de coma flotante: --------------------")
print(numeroComaFlotante)
print(type(numeroComaFlotante))

#tipo String o cadenas de texto ---------------------------
cadena="Hola como estas :)"
print("\nCadena - String : ---------------------------")
print(cadena)
print(type(cadena))

#Tipo booleano verdadero o false ------------------------
booleano=True
print("\nTipo Booleano--------------------------------")
print(booleano)
print(type(booleano))

